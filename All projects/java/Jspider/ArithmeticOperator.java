class ArithmeticOperator 
{
	public static void main(String[] args) 
	{
		String name="Shella";
		System.out.println(name+70);
		System.out.println(name+70+170);
		System.out.println(name+(100+70));
		System.out.println(70+60+name);
		System.out.println(58.7f+23);
		System.out.println(59l+72);
		System.out.println(2525d%3);
		System.out.println('A'+"Hello");
		System.out.println("Hii"+false);
	}
}
